#include "main.h"
/**
 * print_line - function that draws a straight line on the terminal
 * @n: the number of times the character _ is drawn
 * Return: a straight line
 */
void print_line(int n)
{
	int n;

	if (n <= 0)
	{
	_putchar('\n');
	}
	else
	{
	for (n = 0; n <= n; n++)
	{
	putchar(95);
	}
	putchar('\n');
	}
}
